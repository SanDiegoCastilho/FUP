altura = float(input("Digite o valor da altura do trapézio (cm): "))
BaseMaior = float(input("Digite o valor da Base maior do trapézio (cm): "))
BaseMenor = float(input("Digite o valor da base menor do trapézio (cm): "))

area = (BaseMenor + BaseMaior) * altura / 2

print("A área do trapézio equivale a: ",area,"cm²")
